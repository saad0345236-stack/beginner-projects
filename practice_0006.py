# Practicing with if, elif and else statements:
snow_day = False
sunny_day = True
if snow_day:
    print("It's a snow day!")
    print("Wear a jacket.")
elif sunny_day:
    print("It's a sunny day!")
    print("Drink water.")
else:
    print("It's a beautiful day.")
print("Goodbye.")
