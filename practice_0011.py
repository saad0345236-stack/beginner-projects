# Practicing For Loops:
for letter in 'France':
    print(letter)
for item in ['John', 'Sara', 'Chris']:
    print(item)
for integer in [1, 3, 5, 7, 9]:
    print(integer)
for number in range (10, 17, 3):
    print(number)
for item_2 in ['France', 'Germany', 'Ireland']:
    print(item_2)
for digit in range (10, 20, 5):
    print(digit)
for character in 'name':
    print(character)